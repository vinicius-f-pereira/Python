# build array of strings
names = ["John", "Marie", "Anne"]

# print variable or literal value
print(names)

# append a name in array (end)
names.append("Gus")

print(names)

# remove name from a list, just give the name to function
names.remove("John")

print(names)

# to remove an element from a list, give position of it. 
names.pop(2)

print(names)
